public class Q10 {
    public static void main(String[] args) {

        int i = 100;

        System.out.println("==================================: <<");
        System.out.println( i << 1);
        System.out.println( i << 2);
        System.out.println( i << 3);
        System.out.println( i << 4);

        System.out.println("================================: >>");

        System.out.println( i >> 1);
        System.out.println( i >> 2);
        System.out.println( i >> 3);
        System.out.println( i >> 5);


        System.out.println("================================: >>> ");

        System.out.println( i >>> 1);
        System.out.println( i >>> 2);
        System.out.println( i >>> 3);
        System.out.println( i >>> 4);




    }


}